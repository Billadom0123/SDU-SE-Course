package com.sprint.unittesting.unittesting.model;

public class Item {
    // ID
    private Integer id;

    // 名称
    private String name;

    // 宽度
    private int width;

    // 高度
    private int height;

    public Item() {
    }

    public Item(Integer id, String name, int width, int height) {
        this.id = id;
        this.name = name;
        this.width = width;
        this.height = height;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
